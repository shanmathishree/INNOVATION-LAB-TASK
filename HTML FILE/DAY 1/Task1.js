// Declare variables using var, let, and const
var name = "Shanmathi";
let age = 20;
const country = "India";

// Output different messages to the console
console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);

// Modify variables (only var and let)
name = "Shree";
age = 19;

// Observe the changed outputs
console.log("Updated Name:", name);
console.log("Updated Age:", age);
